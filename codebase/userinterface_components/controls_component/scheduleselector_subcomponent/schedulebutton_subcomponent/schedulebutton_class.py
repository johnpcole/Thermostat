from .....common_components.datetime_datatypes import clock_module as Clock

class DefineButton():

	# ==========================================================================================
	# Object Setup
	# ==========================================================================================


	def __init__(self):

		self.time = Clock.createasinteger(0)

		self.temp = -999

	# ==========================================================================================
	# Perform Actions
	# ==========================================================================================



	# -------------------------------------------------------------------
	# Sets the button
	# -------------------------------------------------------------------

	def updatebutton(self, time, temp):

		self.time = Clock.createasclock(time)

		self.temp = temp



	# -------------------------------------------------------------------
	# Returns the time
	# -------------------------------------------------------------------

	def gettime(self):

		return self.time



	# -------------------------------------------------------------------
	# Returns the temp
	# -------------------------------------------------------------------

	def gettemp(self):

		return self.temp

